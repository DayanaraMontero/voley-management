package es.cheste.enums;

/**
 * Este Enum representa las diferentes posiciones de una jugadora en un equipo de voleibol.
 */
public enum PosicionJugadora {

    /**Esta posición es la responsable de organizar las jugadas y
     * distribuir el balón.*/
    COLOCADORA,

    /**Esta posición es la que se encarga de los ataques fuertes desde el
     * lado izquierdo del colocador.*/
    OPUESTO,

    /**Esta posición se encarga de principalmente bloquear y realizar ataques
     * rápidos desde el centro de la red.*/
    CENTRAL,

    /**Esta posición se encarga de la defensa, especialmente en recibir y devolver
     * balones bajos.*/
    LIBERO,

    /**Esta posición se enfoca en realizar ataques y remates al otro lado de la red
     * desde el lado derecho del colocador.*/
    ATACANTE
}
